package goods;

public class FileVO {
	
	private int file_no, writing_no;
	private String  file_name, file_path, writing_type;
	public int getFile_no() {
		return file_no;
	}
	public void setFile_no(int file_no) {
		this.file_no = file_no;
	}
	public int getWriting_no() {
		return writing_no;
	}
	public void setWriting_no(int writing_no) {
		this.writing_no = writing_no;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getWriting_type() {
		return writing_type;
	}
	public void setWriting_type(String writing_type) {
		this.writing_type = writing_type;
	}
	
	
}
